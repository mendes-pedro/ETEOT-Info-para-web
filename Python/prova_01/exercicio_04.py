print("Todos os números pares entre 0 (inclusive) e 15: ", end=" ")
for n1 in range (0,16,2):
    print(n1, end=" ",)
print("\n")
print("Todos os números inteiros entre -10 (inclusive) e + 10 (inclusive): ", end=" ")
for n2 in range (-10,11):
    print(n2, end=" ")
print("\n")
print("Todos os números ímpares entre 15 (inclusive) e zero (inclusive): ", end=" ")
for n3 in range (15,0,-2):
    print(n3, end=" ")
print("\n")
print("Todos os números entre 25 (inclusive) e 37 (inclusive): ", end=" ")
